package services;

public class FetchGamers {
	
	public String username;
	public String name;
	public String email;
	public String dob;
	
	public FetchGamers(String username, String name, String email, String dob) {
		this.username = username;
		this.name = name;
		this.email = email;
		this.dob = dob;
	}
	

}